// Event Bubbling and Target Element

/*  Event Bubbling
    
    When an event is fired on an element 
    --> That event is also fired on all of the parent elements, the ancestors.... (or simply all the way UP in the DOM tree)
*/

/*
    Target Element: is the element on which an event is first fired 
    (or the element that causes the event to happen)

    (or simply say: the element that user clicks on, press key....)
*/